                    <p style="text-align: justify;">
                        <strong>NanoCelle Technology</strong>
                    </p>

                    <p style="text-align: justify;">
                        Nanotechnology converts fat-soluble and poorly water-soluble compounds into highly
                        water-soluble particles. This process creates a clear aqueous solution of these nutrients in
                        high concentrations. This results in very stable nutrient preparations that greatly enhance
                        their absorption and also the rate at which they are absorbed.<sup>1</sup> This in particular
                        allows absorption across the oral mucosal lining as opposed to intestinal absorption.
                    </p>

                    <p style="text-align: justify;">
                        Medlab has achieved a successful world first lodgement for a patent application pertaining to
                        a unique delivery system that converts such compounds into nanosized particles, certified to
                        measure less than 0.2 microns. Nano-formulations, by virtue of their size, are known for
                        circumventing the obstacles of poor pharmacokinetics. The resulting aqueous solubility of the
                        compound ensures its transport across the cellular membrane. This patented technology will be
                        applied to both nutritional and pharmaceutical compounds in the near future.
                    </p>

                    <p style="text-align: justify;">
                        Our research demonstrates that nanotechnology increases plasma and serum levels of fat-soluble
                        nutrients up to 3 times more than emulsions and up to 5 times more than oil preparations in
                        healthy participants (Fig 1). Furthermore, the absorption rate of the nanoparticles is 200%
                        more than that of an emulsion formulation and 370% more than an oil preparation (Fig 2).
                        <sup>2</sup>
                    </p>

                    <br>

