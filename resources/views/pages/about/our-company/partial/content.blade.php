                <div class="panel-body medlab_panel_content">
                    <p>
                        Medlab Clinical Ltd (Medlab) was formed by <a class="medlab_panel_content_link" href="http://www.linkedin.com/in/seanhallfitbio">Sean Hall</a>
                        after he sold his family's companies <a class="medlab_panel_content_link" href="http://www.fit.net.au/">FIT - Bioceuticals</a> LTD and
                        <a class="medlab_panel_content_link" href="http://www.halldrug.com.au/">Hall Drug Technologies</a> to <a class="medlab_panel_content_link" href="http://blackmores.com.au/">Blackmores</a>
                        LTD on the first of July 2012.
                    </p>
                    <p>
                        Medlab is an early phase drug development company based in Sydney, Australia.
                    </p>
                    <p>
                        Medlab specialises in biologics targeting illness, disease states and syndromes. More specifically, Medlab focuses on the role that bacteria play in
                        health and disease and investigates the gastrointestinal microbiome&rsquo;s ability to modulate the impact of other therapeutic agents. Particularly,
                        Medlab embraces a unique area of research to develop new medicines in highly sort areas, specifically: chronic kidney disease, obesity, depression, pain
                        management and anti-aging/muscular-skeletal health. The focus on these specific areas of research is based on our strong belief that biological medicine
                        plays a huge role in rescuing and/or improving symptomatology.
                    </p>
                    <p>
                        Medlab&rsquo;s facility is dedicated to research and therapeutic design. Medlab has a purpose built biologics laboratory, to specifically conduct research
                        into the uses of bacteria, blood and restricted substances. The lab is fully contained with gown and wash-rooms, refrigeration rooms regulated at different
                        temperatures, testing and bench areas, as well as dry chemical and storage rooms. The various storage equipment such as the Nitrogen filled vats and
                        -80?C freezers are stored outside the lab environment.
                    </p>
                    <p>
                        In addition, Medlab has an on-site blood drawing room. This provides Medlab with the ability to draw blood on site and send the blood into the lab for
                        immediate assay.
                    </p>
                    <p>
                        Medlab's laboratory supports pre-commercialisation, including clinical trials and bench work, which enables early phase drug discovery with the intention
                        of creating five first-in-class drugs.
                    </p>
                    <p>
                        Medlab is a proud member of <a class="medlab_panel_content_link" href="http://www.ausbiotech.org/">AusBiotech</a>,
                        <a class="medlab_panel_content_link" href="http://medicinesaustralia.com.au/">Medicines Australia</a> and
                        <a class="medlab_panel_content_link" href="http://www.internationalprobiotics.org/">IPA</a>. Medlab is also collaborating with the Boden Institute
                        of Obesity, Nutrition, Exercise and Eating Disorders, and the University of Sydney. Medlab have 9 patents lodged representing our projects in biological
                        development.
                    </p>
                    <p>
                        Medlab have a strong desire and belief in the successful outcome of the proposed research. They are clearly on the frontier of medicine.
                    </p>
                    <p>
                        As of September 2014, Medlab extended its offerings to include novel nutraceuticals aimed at specific patient populations. The nutraceutical extension
                        was made possible through the ongoing research at Medlab and its partial owned US subsidiary <a class="medlab_panel_content_link" href="http://www.inmedtech.co/">InMed Technologies</a>.
                    </p>
                    <p>
                        <strong>Medlab&hellip; Part of the Hall Family History</strong>
                    </p>
                    <p>
                        Michael Hall was one of the founders of Bioglan (<a class="medlab_panel_content_link" href="http://www.bioglan.com.au">www.bioglan.com.au</a>) and later
                        Pretorius Vitamins. He did this because of his own personal experience with cancer, which led him to believe that other factors such as lifestyle and
                        nutrition were important helpers to orthodox medicine. Michael&rsquo;s interest in Vitamin C led him to the Linus Pauling Institute where he met a brilliant
                        university professor based in Washington State; Dr. Jeffrey Bland. Dr. Jeffrey Bland and Dr. David Rutolo joined Michael as consultants; both have since
                        made major contributions to nutrition knowledge.
                    </p>
                    <p>
                        Investing in USA research, Michael partnered with Dr. David Rutolo in development of Micelle technology. Joining Dr. Bland, HealthComm Inc. (USA) was
                        established, best known in Australia for UltraMeal, UltraClear and UltraClear Sustain functional foods. Through Health Comm and Michael Hall&rsquo;s
                        involvement in the USA, alliances were formed with <a class="medlab_panel_content_link" href="http://www.gdx.net/">Great Smokies Diagnostic Laboratories</a>
                        (GSDL).
                    </p>
                    <p>
                        Back in Australia, Michael started NutriSearch. Sean Hall was brought onboard in Sales and Marketing. Michael, an accountant by trade, then acquired Vitaplex
                        out of receivership and together with Sean, set about restoring the brand and preparing it for sale. Alex and Sean Hall distributed HealthComm products for
                        Health Comm Inc. in Australia and New Zealand. During this time, they introduced Great Smokies Diagnostic Laboratories and pioneered functional pathology
                        testing for practitioners to Australia and New Zealand. Metagenics later purchased HealthComm&rsquo;s distribution.
                    </p>
                    <p>
                        In 1999, Alex and Sean Hall launched <a class="medlab_panel_content_link" href="http://www.bioceuticals.com.au/">BioCeuticals</a> with four products;
                        Herbstat, Paracea, Bactacea and UTIcea, formulated with the support of Ellanor Barrager. Together, they ran world-class conferences under the Institute
                        of Functional Medicine. In seven years, BioCeuticals grew to be one of the most successful &ldquo;Practitioner Only&rdquo; companies in Australia.
                        In September 2007, Alex Hall sadly passed away from cancer.
                    </p>
                    <p>
                        After working diligently with the <a class="medlab_panel_content_link" href="http://www.nehta.gov.au/">National E-Health Transition Authority</a>
                        (NEHTA), <a class="medlab_panel_content_link" href="http://www.bioceuticals.com.au/">BioCeuticals</a> sought and achieved recognition for complementary
                        medicines from an inclusion in the National Product Catalogue (NPC), making <a class="medlab_panel_content_link" href="http://www.bioceuticals.com.au/">BioCeuticals</a>
                        the only nutritional medicine approved for use in Australian hospitals. Less than a year later, in June 2008, Sean announced that BioCeuticals and our sister company
                        PharmaFoods had merged; both BioCeuticals and Pharmafoods brands were incorporated under one parent company name
                        <a class="medlab_panel_content_link" href="http://www.bioceuticals.com.au/">FIT-BioCeuticals</a>.
                    </p>
                    <p>
                        Soon after, a close collaboration was formed with two independent research team, one at the <a class="medlab_panel_content_link" href="http://www.alfred.org.au/">Alfred Hospital</a>
                        (Prof. Frank Rosenfedlt), in Melbourne and the second at <a class="medlab_panel_content_link" href="http://www.uq.edu.au/">the University of Queensland</a> (Prof. Luis Vitetta).
                        Later the <a class="medlab_panel_content_link" href="http://www.chw.edu.au/">Children&rsquo;s Hospital Westmead</a> worked with BioCeuticals in a collaborative Pediatric
                        study for the first drug under the Hall Drug Technologies (HDT) Label, D50K.
                    </p>
                    <p>
                        With the ever-growing popularity of IsoWhey, and efforts to move the PharmaFoods and BioCeuticals brands forward, a clear positioning strategy was implemented
                        for the first three brands:
                    </p>
                    <ol>
                        <li>BioCeuticals - always maintained its position as a &ldquo;Practitioner Only Range&rdquo; and continued to go from strength to strength in core markets with medical doctors, naturopaths, chiropractors and other healthcare practitioners.</li>
                        <li>IsoWhey: Has a long history and strong following on the TVSN shopping network, and as such it was appropriate to position as a retail brand, making it available in retail pharmacies and health food stores.</li>
                        <li>PharmaFoods Professional - was launched in October 2008, and was positioned as a professional pharmacy range.&nbsp; This brand targets pharmacists and pharmacy assistants in professional pharmacies.&nbsp;</li>
                    </ol>
                    <p>
                        In February 2009, BioCeuticals mainstreamed operations in New Zealand, opening an office in Auckland to allow New Zealand practitioners to deal directly with
                        BioCeuticals.
                    </p>
                    <p>
                        FIT-BioCeuticals was involved in numerous charity events, major educational events and support groups for practitioners. FIT-BioCeuticals LTD grew to 120+
                        staff, and in July 2012 the Hall family sold to Blackmores LTD at a premium price.
                    </p>
