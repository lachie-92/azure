                        biological medicine plays a huge role in rescuing and/or improving symptomatology. The most advanced work is on obesity and depression where clinical
                        trials are in progress in Collaboration with the <a class="medlab_panel_content_link" href="http://sydney.edu.au/">University of Sydney</a>, the
                        <a class="medlab_panel_content_link" href="http://www.materhospital.com.au/">Mater Hospital</a> in Brisbane as well as The Boden Institute of Obesity,
                        Nutrition, Exercise and Eating Disorders.
                    </p>
                    <p>
                        Medlab's current research portfolio covers areas across obesity, chronic kidney disease, depression, muscular loss associated with ageing and
                        non-opioid pain management.
