                        specifically targeting conditions previous thought out of realm for biological medicine. The sophistication of the lab facilities allows Medlab to expand
                        into pharmaceutical and nutraceutical analytical work. Specifically, the lab supports pre-commercialisation, including clinical trials and bench
                        work, which enables early phase drug discovery with the intention of creating first-in-class drugs.
                    </p>
