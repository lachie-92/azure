                    <p>
                        Luis Vitetta is currently Director of Medical Research at MedLab in Sydney and&nbsp;Adjunct Professor at the University of Sydney, Sydney Medical School.
                    </p>
                    <p>
                        <strong>
                            Expertise: Probiotics / Human Microbiome, Nutraceuticals, Nutrition, Clinical trials / Epidemiology, Integrative Medicine and Complementary and Alternative Medicine.
