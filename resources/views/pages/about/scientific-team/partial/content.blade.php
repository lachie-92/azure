                    <p>
                        Between the years 2007-2013 he was the Director and A/Professor of the Centre for Integrative Clinical and Molecular Medicine at The University of Queensland, School of Medicine.
                    </p>
                    <p>
                        Luis is an Honours Graduate from Monash University (1981) in the Department of Biochemistry - Faculty of Medicine, and a PhD Graduate from the University of Melbourne and Faculty of Medicine's Department of Surgery (1985) where he studied the epidemiological and aetiological factors of biliary disease. He has also studied medicine at the National University of La Plata Medical School in his native Argentina.
                    </p>
                    <p>
                        He was the chief investigator and the recipient of a National Institute of Complementary Medicine grant for the NICM Collaborative Centre for Transitional/Preclinical and Clinical Research in Nutraceuticals and Herbal Medicines. [<a class="medlab_panel_content_link" href="http://www.uq.edu.au/news/?article=14539">http://www.uq.edu.au/news/?article=14539</a>]
                    </p>
                    <p>
                        Previously he was a Senior Research Fellow at the Centre for Molecular Biology and Medicine, Epworth Medical Centre in Melbourne.
                    </p>
                    <p>
                        Between the years of 2000&ndash;2005 he was the Deputy Head and Director of Research of the Graduate School of Integrative Medicine at Swinburne University, where he assisted in implementing post graduate courses for medical graduates in the areas of Nutrition and Environmental Medicine and Integrative Medicine.
                    </p>
                    <p>
                        He has served as a member of the Cancer Council&rsquo;s Epidemiology group, an alternate Director of the Victorian Public Health Research and Education Council, and member of its research committee. He was a member of the executive board of the Australian Research Centre for Complementary and Alternative Medicine. He was also an honorary affiliate with the Department of Medicine, Nursing and Health Sciences, Monash University.
                    </p>
                    <p>
                        Previous appointments have included, Senior Research Associate with the University of Melbourne&rsquo;s Faculty of Medicine - Centre for Palliative Care at Caritas Christi Hospice and St Vincent&rsquo;s Hospital and a University of Melbourne Research Fellow with the Faculty of Medicine and the Department of Surgery at the Austin and Repatriation Medical Centre.
                    </p>
                    <p>
                        Professor Vitetta has research interests in nutrition, functional foods and immune function, as well as Mind Body Medicine. He was the first internationally funded Australian researcher investigating Chinese Herbal Medicines in a trial with HIV patients. He is often an invited speaker at local and international conferences.
                    </p>

