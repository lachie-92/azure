                    <h3 id="answer1">
                        <strong>EnBiotic</strong><sup>TM</sup>
                    </h3>

                    <br>

                    <p>
                        <strong>Does EnBiotic<sup>TM</sup> need to berefrigerated?</strong>
                    </p>

                    <p>
                        As EnBiotic<sup>TM</sup> contains live bacteria we recommend it be stored in the refrigerator
                        to keep the bacteria stable for as long as possible. In saying this, short periods of
                        non-refrigeration (if not exposed to heat) will not harm the product.
                    </p>

                    <p>
                        <strong>What time of day should EnBiotic<sup>TM</sup> be taken?</strong>
                    </p>

                    <p>
                        EnBiotic<sup>TM</sup> should be taken before meals i.e. before breakfast, lunch and dinner, up
                        to three times per day.
                    </p>

                    <p>
                        <strong>From what age can I take EnBiotic<sup>TM</sup>?</strong>
                    </p>

                    <p>
                        EnBiotic<sup>TM</sup> is recommended for adults only. Please consult your healthcare
                        practitioner for individual dosage recommendations.
                    </p>

                    <p>
                        <strong>What is the dosage recommendation?</strong>
                    </p>

                    <p>
                        For adults, take two capsules with water daily before meals, up to three times per day.
                    </p>

                    <p>
                        <strong>Is EnBiotic<sup>TM</sup> Dairy and Gluten Free?</strong>
                    </p>

                    <p>
                        Yes
                    </p>

                    <p>
                        <strong>Is EnBiotic<sup>TM</sup> suitable for Vegetarians?</strong>
                    </p>

                    <p>
                        Yes. The capsules are made from vegetable fiber, not from gelatin.
                    </p>

